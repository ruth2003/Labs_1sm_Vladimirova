#include <iostream>

int main() {
    setlocale(LC_ALL, "Russian");
    unsigned int x;
    int i;

    std::cout << "Введите число x: ";
    std::cin >> x;

    std::cout << "Введите номер бита i (0-31): ";
    std::cin >> i;

    if (i < 0 || i > 31) {
        std::cout << "Номер бита должен быть в диапазоне от 0 до 31." << std::endl;
        return 1;
    }

    // Создаем маску, в которой только i-й бит равен 1
    unsigned int mask = 1 << i;

    // Устанавливаем i-й бит в 1
    x |= mask;

    std::cout << "Число x после установки " << i << "-го бита в 1: " << x << std::endl;

    return 0;
}

